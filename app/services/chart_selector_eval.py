"""Bulk chart_selector evaluation: scores every dataset in the corpus.

Two consumers:

- `scripts/build_chart_selector_baseline.py` — generates the baseline JSON
  that gets committed to the repo.
- `tools/tempo-dev-mcp` `tempo_eval_chart_selector` — runs the same
  evaluation on the current code and diffs against the committed baseline
  to surface regressions when chart_selector logic changes.

The shared `evaluate_all()` keeps both consumers in lock-step so a baseline
generated by the script will exactly match what the MCP tool computes.
"""
from __future__ import annotations

from app.db import get_conn
from app.services.chart_selector import build_signature, select_charts


def _load_inputs(conn):
    """Bulk-load every input chart_selector needs, keyed by matrix_code.

    One DuckDB query per table — much faster than per-dataset round-trips
    via `get_dataset_meta` (the latter is ~10ms × 1959 = 20s; this approach
    is ~1s for the whole corpus).
    """
    profile_cols = [c[0] for c in conn.execute("DESCRIBE matrix_profiles").fetchall()]
    profiles = {
        r[0]: dict(zip(profile_cols, r))
        for r in conn.execute("SELECT * FROM matrix_profiles").fetchall()
    }

    # Per-dimension type via majority vote on parsed options.
    # Tie-breaker: on equal counts, pick the dim_type whose first option has
    # the lowest option_offset. This matches dataset_meta.py:172 where
    # `max(type_counts, key=...)` returns the first-inserted key on ties
    # (and insertion order there is ORDER BY option_offset).
    dim_type_map: dict[int, str] = {}
    for dim_id, dtype, _cnt, _min_off in conn.execute("""
        SELECT dopt.dimension_id,
               dop.dim_type,
               COUNT(*) AS cnt,
               MIN(dopt.option_offset) AS min_off
        FROM dimension_options dopt
        JOIN dimension_options_parsed dop ON dopt.nom_item_id = dop.nom_item_id
        GROUP BY dopt.dimension_id, dop.dim_type
        ORDER BY dopt.dimension_id, cnt DESC, min_off ASC
    """).fetchall():
        if dim_id not in dim_type_map:
            dim_type_map[dim_id] = dtype

    dims_by_matrix: dict[str, list] = {}
    for dim_id, mc, col, opt_count in conn.execute(
        "SELECT dimension_id, matrix_code, dim_column_name, option_count FROM dimensions"
    ).fetchall():
        dims_by_matrix.setdefault(mc, []).append({
            "dimension_id": dim_id,
            "dim_column_name": col,
            "option_count": opt_count,
            "dim_type": dim_type_map.get(dim_id),
        })

    cov_cols = [c[0] for c in conn.execute("DESCRIBE dataset_coverage").fetchall()]
    coverages = {
        r[0]: dict(zip(cov_cols, r))
        for r in conn.execute("SELECT * FROM dataset_coverage").fetchall()
    }

    vp_cols = [c[0] for c in conn.execute("DESCRIBE dataset_value_profiles").fetchall()]
    value_profiles = {
        r[0]: dict(zip(vp_cols, r))
        for r in conn.execute("SELECT * FROM dataset_value_profiles").fetchall()
    }

    tr_cols = [c[0] for c in conn.execute("DESCRIBE dataset_trends").fetchall()]
    trends = {
        r[0]: dict(zip(tr_cols, r))
        for r in conn.execute("SELECT * FROM dataset_trends").fetchall()
    }

    matrices = [r[0] for r in conn.execute(
        "SELECT matrix_code FROM matrices ORDER BY matrix_code"
    ).fetchall()]

    return matrices, profiles, dims_by_matrix, coverages, value_profiles, trends


def evaluate_all(conn=None, *, top_n: int = 3) -> dict:
    """Score every dataset in `matrices` and return a compact summary.

    Returns: {
        matrix_code: {
            "archetype":  str,
            "primary":    str,    # top-ranked chart_type
            "confidence": str,    # 'high' | 'medium' | 'low' | None
            "topN":       [[chart_type, score], ...]   # length up to top_n
        }
    }
    """
    if conn is None:
        conn = get_conn()
    matrices, profiles, dims_by_matrix, coverages, value_profiles, trends = _load_inputs(conn)

    out: dict[str, dict] = {}
    for mc in matrices:
        sig = build_signature(
            profiles.get(mc, {}),
            dims_by_matrix.get(mc, []),
            coverages.get(mc),
            value_profiles.get(mc),
            trends.get(mc),
        )
        ranked = select_charts(sig)
        if not ranked:
            out[mc] = {
                "archetype": sig.get("_archetype"),
                "primary": "table",
                "confidence": None,
                "topN": [],
            }
            continue

        out[mc] = {
            "archetype": sig.get("_archetype"),
            "primary": ranked[0]["chart_type"],
            "confidence": ranked[0].get("confidence"),
            "topN": [[r["chart_type"], round(float(r["score"]), 4)] for r in ranked[:top_n]],
        }
    return out


def diff_against_baseline(baseline: dict, current: dict, *, score_threshold: float = 0.05) -> dict:
    """Compare current evaluation to a baseline and return a regression report.

    The report is intentionally compact (capped lists) so it fits inside a
    single MCP tool response.

    Args:
        baseline:        {matrix_code: {archetype, primary, confidence, topN}}
        current:         same shape, freshly computed
        score_threshold: minimum primary-score delta to flag as drift

    Returns: {
        summary:           {total, ok, primary_changed, top_set_changed,
                            confidence_changed, score_drift, missing, added},
        primary_changes:   [{matrix_code, baseline: {…}, current: {…}}, …],
        top_set_changes:   [{matrix_code, baseline: [chart…], current: [chart…]}, …]   (cap 50)
        confidence_changes:[{matrix_code, baseline_conf, current_conf, primary}, …]   (cap 30)
        score_drifts:      [{matrix_code, primary, baseline_score, current_score, delta}, …] (cap 50)
        missing:           [matrix_code, …]   (in baseline but not in current)
        added:              [matrix_code, …]   (in current but not in baseline)
    }
    """
    primary_changes = []
    top_set_changes = []
    confidence_changes = []
    score_drifts = []
    missing = []
    added = []
    ok = 0

    for mc, b in baseline.items():
        c = current.get(mc)
        if not c:
            missing.append(mc)
            continue

        primary_match = b.get("primary") == c.get("primary")
        b_set = [t[0] for t in (b.get("topN") or [])]
        c_set = [t[0] for t in (c.get("topN") or [])]
        top_set_match = set(b_set) == set(c_set)

        b_top_score = (b.get("topN") or [[None, 0]])[0][1]
        c_top_score = (c.get("topN") or [[None, 0]])[0][1]
        score_delta = round(c_top_score - b_top_score, 4)

        conf_match = b.get("confidence") == c.get("confidence")

        if not primary_match:
            primary_changes.append({
                "matrix_code": mc,
                "baseline": {"primary": b.get("primary"), "score": b_top_score, "archetype": b.get("archetype")},
                "current":  {"primary": c.get("primary"), "score": c_top_score, "archetype": c.get("archetype")},
            })
            continue

        if not top_set_match:
            top_set_changes.append({
                "matrix_code": mc,
                "baseline": b_set,
                "current": c_set,
            })
            continue

        if not conf_match:
            confidence_changes.append({
                "matrix_code": mc,
                "primary": c.get("primary"),
                "baseline_confidence": b.get("confidence"),
                "current_confidence": c.get("confidence"),
            })
            continue

        if abs(score_delta) >= score_threshold:
            score_drifts.append({
                "matrix_code": mc,
                "primary": c.get("primary"),
                "baseline_score": b_top_score,
                "current_score": c_top_score,
                "delta": score_delta,
            })
            continue

        ok += 1

    for mc in current:
        if mc not in baseline:
            added.append(mc)

    return {
        "summary": {
            "total_baseline": len(baseline),
            "total_current": len(current),
            "ok": ok,
            "primary_changed": len(primary_changes),
            "top_set_changed": len(top_set_changes),
            "confidence_changed": len(confidence_changes),
            "score_drift": len(score_drifts),
            "missing": len(missing),
            "added": len(added),
            "score_threshold": score_threshold,
        },
        "primary_changes": primary_changes,  # always full — most important signal
        "top_set_changes": top_set_changes[:50],
        "confidence_changes": confidence_changes[:30],
        "score_drifts": sorted(score_drifts, key=lambda x: abs(x["delta"]), reverse=True)[:50],
        "missing": missing[:30],
        "added": added[:30],
    }
